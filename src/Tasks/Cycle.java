package Tasks;
// ДЕЛАТЬ ЧТО-ТО СНОВА И СНОВА
 //ЦИКЛЫ: for и while
public class Cycle {
    public static void main(String[] args) {
        int x = 3;
        while (x > 12) {
            x = x - 1;
        }
        for ( x = 0; x <10; x = x +1 ) {
            System.out.println( "Теперь х равен" + x);
        }
    }
}